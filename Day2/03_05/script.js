/**
 * Create a Backpack object.
 */

const backpack = {
  name: "Everyday Backpack",
  volume: 30,
  color: "grey",
  pocketNum: 15,
  strapLength: {
    left: 26,
    right: 26,
  },
  lidOpen: false,
  toggleLid: function (lidStatus) {
    this.lidOpen = lidStatus;
  },
  newStrapLength: function (lengthLeft, lengthRight) {
    this.strapLength.left = lengthLeft;
    this.strapLength.right = lengthRight;
  },
};
console.log("The Dot notation access for pocket numbers:",backpack.pocketNum);
console.log("the bracket notation access for pocket num:",backpack["pocketNum"]);
var query = "pocketNum";
console.log("the bracket notation access for pocket num:",backpack[query]);
